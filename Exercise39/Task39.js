// making function
function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
// calling function with Adding values:
var City1 = city_country("Lahore", "Pakistan");
console.log(City1);
var City2 = city_country("Sharjah", "Dubai");
console.log(City2);
var City3 = city_country("Izmir", "Turkey");
console.log(City3);
var City4 = city_country("Islamabad", "Pakistan");
console.log(City4);
